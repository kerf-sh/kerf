def _parse_selig_text(text: str) -> np.ndarray:
    """Parse inline Selig-format coordinate text into an ndarray."""
    lines = text.strip().splitlines()
    # Skip the header (first non-empty line)
    data_lines = []
    header_skipped = False
    for line in lines:
        line = line.strip()
        if not line:
            continue
        if not header_skipped:
            # Check if it looks like coordinate data (starts with digit or minus)
            try:
                float(line.split()[0])
                header_skipped = True
                data_lines.append(line)
            except ValueError:
                header_skipped = True  # skip this header
                continue
        else:
            data_lines.append(line)

    if not data_lines:
        raise ValueError("No coordinate data found in Selig text")

    coords = []
    for line in data_lines:
        parts = line.split()
        if len(parts) >= 2:
            coords.append((float(parts[0]), float(parts[1])))

    return np.array(coords, dtype=float)


def selig_load(name: str) -> np.ndarray:
    """
    Load a curated Selig-format airfoil by slug name.

    Parameters
    ----------
    name : str
        Slug key from :data:`SELIG_SLUGS`, e.g. ``"naca0012"``,
        ``"e387"``, ``"clarky"``.

    Returns
    -------
    coords : np.ndarray, shape (N, 2)
        ``(x, y)`` coordinate pairs.  Leading edge near ``(0, 0)``,
        trailing edge near ``(1, 0)``.

    Raises
    ------
    KeyError
        If ``name`` is not found in the curated database.
    """
    key = name.lower().strip()
    if key not in _RAW:
        available = sorted(_RAW.keys())
        raise KeyError(
            f"Airfoil {name!r} not found. Available: {available}"
        )
    return _parse_selig_text(_RAW[key])


