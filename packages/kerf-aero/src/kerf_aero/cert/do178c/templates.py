def generate_template(doc_type: str, project_meta: dict[str, Any] | None = None) -> str:
    """Return a Markdown skeleton for *doc_type* with placeholders filled.

    Parameters
    ----------
    doc_type:
        One of ``SUPPORTED_DOC_TYPES`` (e.g. ``"PSAC"``, ``"SDP"``).
        Case-insensitive.
    project_meta:
        Optional dict of project-level values.  Recognised keys:
        ``project_name``, ``dal``, ``aircraft_type``, ``applicant``,
        ``version``, ``date``, ``author``, ``dqa``.  Unknown keys are
        ignored.  Missing keys produce ``[FILL IN]`` in the output.

    Returns
    -------
    str
        Markdown document with ``[FILL IN]`` placeholders for any
        project-meta fields not supplied.

    Raises
    ------
    ValueError
        If *doc_type* is not in ``SUPPORTED_DOC_TYPES``.
    """
    key = doc_type.strip().upper()
    if key not in _TEMPLATES:
        valid = ", ".join(SUPPORTED_DOC_TYPES)
        raise ValueError(
            f"Unknown DO-178C doc_type {doc_type!r}. Valid values: {valid}"
        )

    meta: dict[str, Any] = dict(project_meta or {})
    meta["doc_type_title"] = _DOC_TITLES[key]
    # Ensure DAL placeholder is consistent
    if "dal" not in meta:
        meta["dal"] = "[FILL IN]"

    return _sub(_TEMPLATES[key], meta)
