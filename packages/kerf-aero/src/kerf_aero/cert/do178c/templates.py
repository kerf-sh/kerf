def _sub(template: str, meta: dict[str, Any]) -> str:
    """Substitute *meta* values into *template*.

    Each occurrence of ``{{key}}`` is replaced with ``meta[key]`` if
    the key is present, otherwise it is replaced with ``[FILL IN]``.
    """
    import re

    def _replace(m: "re.Match[str]") -> str:
        key = m.group(1).strip()
        return str(meta.get(key, "[FILL IN]"))

    return re.sub(r"\{\{(\w+)\}\}", _replace, template)


