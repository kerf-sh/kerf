# ---------------------------------------------------------------------------
# Sanity-check: none of the extended entries shadow the base DB
# (called at import time to catch key collisions early)
# ---------------------------------------------------------------------------
_COLLISIONS = set(_EXT.keys()) & set(_BASE_DB.keys())
if _COLLISIONS:  # pragma: no cover
    raise ValueError(
        f"extended_db.py entries shadow base db.py keys: {sorted(_COLLISIONS)}"
    )


# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------

def get_extended_db() -> dict[str, dict]:
    """Return only the extended (new) material entries."""
    return dict(_EXT)


def get_full_db() -> dict[str, dict]:
    """Return the union of the base database and extended materials.

    Keys from the base db are never overwritten.
    """
    combined: dict[str, dict] = {}
    combined.update(_BASE_DB)
    combined.update(_EXT)
    return combined
